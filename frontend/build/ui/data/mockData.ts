export function getCaseById(id: string): MockCase | undefined {
  const c = mockCases.find((c) => c.id === id);
  if (!c) return undefined;
  return {
    ...c,
    documents: c.documents ?? DEFAULT_CASE_DOCUMENTS(c.id, c.createdAt),
  };
}

export function getUserById(id: string): MockUser | undefined {
  return mockUsers.find((u) => u.id === id);
}

export function getNotificationsForRole(role: string): MockNotification[] {
  return mockNotifications.filter((n) =>
    n.visibleTo.includes(role as MockNotification['visibleTo'][number])
  );
}
